/**
 * This is the first Kotlin program in the learning series.
 * In this program, we explore the `println()` function.
 *
 * - `println("Hello World!")` prints the text inside the parentheses.
 * - `println()` is used to add a blank line.
 * - `println("My first Kotlin program!")` prints another line of text.
 */

fun main() {
    // Prints "Hello World!" to the console
    println("Hello World!")

    // Prints a blank line
    println()

    // Prints "My first Kotlin program!" to the console
    println("My first Kotlin program!")
}
